package com.flyingcode.problem;

/**
 * 斐波那契数列暴力递归实现
 * 该类提供了一个基于暴力递归算法的斐波那契数列求解实现
 * 斐波那契数列定义为:F(0)=0, F(1)=1, F(n)=F(n-1)+F(n-2)(n>=2)
 * 暴力递归是最直观的实现方式, 但时间效率较低
 */
public class Algo014FibonacciBrute {
  /**
   * 算法设计思路:
   * 1. 基于斐波那契数列的数学定义直接实现递归
   * 2. 基本情况: 当n<=1时, 直接返回n(F(0)=0, F(1)=1)
   * 3. 递归情况: 对于n>=2, 通过递归调用计算F(n-1)和F(n-2), 然后将结果相加
   * 4. 采用自顶向下的递归方式, 从目标值n开始逐步分解为更小的子问题
   */

  /**
   * 算法复杂度分析:
   * - 时间复杂度:O(2^n), 其中n是输入的数值
   * 因为每一层递归都会产生两个新的递归调用, 递归树的高度为n, 节点总数约为2^n
   * - 空间复杂度:O(n), 其中n是输入的数值
   * 递归调用栈的深度等于递归树的高度, 即n, 因此只使用了线性的额外空间
   */

  /**
   * 计算斐波那契数列的第n项值
   *
   * @param n 要计算的斐波那契数列项数,n>=0
   * @return 斐波那契数列的第n项值
   */
  public int solution(int n) {
    // 基本情况:n<=1时直接返回n
    if (n <= 1) {
      return n;
    }
    // 递归情况:F(n) = F(n-1) + F(n-2)
    return solution(n - 1) + solution(n - 2);
  }
}
