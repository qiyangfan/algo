package com.flyingcode.search;

/**
 * 顺序查找
 * 该类提供一个简单的顺序查找算法，用于在整数数组中查找目标值.
 * 算法采用线性遍历技术，实现了直观的查找功能，空间复杂度为 O(1).
 */
public class SequentialSearch {

  /**
   * 算法设计思路:
   * 1. 从数组的第一个元素开始遍历
   * 2. 逐个比较当前元素与目标值
   * 3. 如果找到匹配的元素，返回其索引
   * 4. 如果遍历完整个数组仍未找到，返回-1
   *
   * 关键技术点:
   * - 线性遍历: 逐个检查每个元素
   * - 边界条件处理: 空数组或null数组的处理
   */

  /**
   * 算法复杂度分析:
   * - 时间复杂度: O(n)，其中 n 是数组的长度.
   * 最好情况下，目标元素在数组的第一个位置，时间复杂度为 O(1)
   * 最坏情况下，目标元素不在数组中或在最后一个位置，时间复杂度为 O(n)
   * 平均情况下，时间复杂度为 O(n/2)，即 O(n)
   * - 空间复杂度: O(1).
   * 只使用了常量级的额外空间，没有使用额外的数据结构
   */

  /**
   * 在整数数组中查找目标值的索引
   *
   * @param arr    输入的整数数组，可以包含负数、零和正数，无需排序
   * @param target 要查找的目标值
   * @return 如果找到目标值，返回其索引；否则返回-1
   */
  public int search(int[] arr, int target) {
    // 边界条件处理: 空数组或null数组直接返回-1
    if (arr == null || arr.length == 0) {
      return -1;
    }

    // 线性遍历数组，逐个比较元素
    for (int i = 0; i < arr.length; i++) {
      // 如果当前元素等于目标值，返回索引
      if (arr[i] == target) {
        return i;
      }
    }

    // 遍历完整个数组仍未找到，返回-1
    return -1;
  }
}
