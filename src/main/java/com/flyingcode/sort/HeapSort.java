package com.flyingcode.sort;

/**
 * 堆排序
 * 该类提供一个高效的排序算法, 用于对整数数组进行排序.
 * 算法采用二叉堆数据结构, 通过构建最大堆并逐步取出堆顶元素实现排序, 实现不稳定排序.
 */
public class HeapSort {

  /**
   * 算法设计思路:
   *
   * 堆排序分为两个核心阶段:
   *
   * 1. 构建最大堆阶段:
   * a. 从最后一个非叶子节点开始, 自底向上遍历所有非叶子节点
   * b. 对每个非叶子节点调用 heapify 方法, 调整其子树为最大堆
   * c. 该阶段结合了迭代和递归: 迭代用于批量处理非叶子节点, 递归用于调整子树
   * d. 最终得到一个完整的最大堆, 堆顶元素为数组最大值
   *
   * 2. 排序阶段:
   * a. 将堆顶元素与堆的最后一个元素交换, 最大值被放到数组末尾
   * b. 调整剩余元素使其重新成为最大堆(只需要对根节点调用一次 heapify)
   * c. 重复上述步骤, 直到堆的大小为1
   * d. 该阶段只需要对根节点调用 heapify, 因为其他节点仍保持堆的性质
   *
   * 关键技术点:
   * - 二叉堆性质: 父节点值大于等于子节点值(最大堆)
   * - 数组表示二叉堆: 对于索引为 i 的节点, 左子节点索引为 2*i+1, 右子节点索引为 2*i+2
   * - 迭代+递归结合: 充分利用两种控制结构的优势, 实现高效堆排序
   */

  /**
   * 算法复杂度分析:
   * - 时间复杂度: O(n log n), 其中 n 是数组长度.
   * - 构建堆: O(n), 线性时间复杂度
   * - 堆调整: 每次 O(log n), 共 n-1 次, 总时间 O(n log n)
   * - 空间复杂度: O(1).
   * - 原地排序, 只需要常数级的额外空间
   * - 递归调用栈深度为 O(log n), 但被常数级空间复杂度覆盖
   */

  /**
   * 对整数数组进行堆排序
   *
   * @param arr 待排序的整数数组
   * @return 排序后的整数数组
   */
  public int[] sort(int[] arr) {
    // 边界情况处理: 空数组或长度小于等于1的数组直接返回
    if (arr == null || arr.length <= 1) {
      return arr;
    }

    int n = arr.length;

    // 第一阶段: 构建最大堆
    // 从最后一个非叶子节点开始, 自底向上遍历所有非叶子节点
    // 结合迭代和递归: 迭代用于批量处理节点, 递归用于调整子树
    for (int i = n / 2 - 1; i >= 0; i--) {
      // 对每个非叶子节点调用heapify, 将其子树调整为最大堆
      heapify(arr, n, i);
    }

    // 第二阶段: 排序阶段
    // 逐步将堆顶元素(最大值)与堆的最后一个元素交换
    for (int i = n - 1; i > 0; i--) {
      // 交换堆顶元素和当前堆的最后一个元素
      int temp = arr[0];
      arr[0] = arr[i];
      arr[i] = temp;

      // 只需要对根节点调用一次heapify
      // 因为其他节点仍保持堆的性质, 只有根节点被破坏
      heapify(arr, i, 0);
    }

    return arr;
  }

  /**
   * 调整堆结构, 使其成为最大堆
   * 采用递归方式自顶向下调整子树
   *
   * @param arr 待调整的数组
   * @param n   堆的大小
   * @param i   要调整的节点索引
   */
  private void heapify(int[] arr, int n, int i) {
    int largest = i; // 初始化当前节点为最大值
    int left = 2 * i + 1; // 左子节点索引
    int right = 2 * i + 2; // 右子节点索引

    // 如果左子节点大于当前节点, 更新最大值索引
    if (left < n && arr[left] > arr[largest]) {
      largest = left;
    }

    // 如果右子节点大于当前最大值, 更新最大值索引
    if (right < n && arr[right] > arr[largest]) {
      largest = right;
    }

    // 如果最大值不是当前节点, 说明需要调整
    if (largest != i) {
      // 交换当前节点和最大值节点
      int temp = arr[i];
      arr[i] = arr[largest];
      arr[largest] = temp;

      // 递归调整受影响的子树
      // 因为交换后, 被交换的子树可能不再满足最大堆性质
      heapify(arr, n, largest);
    }
  }
}
